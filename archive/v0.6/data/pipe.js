function arrayOfPipeStandards() {
    let arrayOfPipeStandardsWithDuplicates = PIPE_DIMENSIONS.map((pipeObject, index, array) => {return pipeObject.standard});
    let arrayOfPipeStandardsWithoutDuplicates = [];
    for (let standard of arrayOfPipeStandardsWithDuplicates) {arrayOfPipeStandardsWithoutDuplicates.includes(standard) ? "" : arrayOfPipeStandardsWithoutDuplicates.push(standard)};
    return arrayOfPipeStandardsWithoutDuplicates;
};

function arrayOfPipeNominalDiameter(pipeStandard) {
    let arrayOfPipeObjectsWithUserSelectedStandard = PIPE_DIMENSIONS.filter((pipeObject, index, array) => pipeObject.standard === pipeStandard);
    let arrayOfPipeNominalDiametersWithUserSelectedStandard = arrayOfPipeObjectsWithUserSelectedStandard.map((pipeObject, index, array) => {return pipeObject.nominalDiameter});
    return arrayOfPipeNominalDiametersWithUserSelectedStandard;
};

function arrayOfPipeSchedule(pipeStandard, pipeNominalDiameter) {
    //console.log([pipeStandard, pipeNominalDiameter]);
    let pipeObjectWithUserSelectedStandardAndDiameter = (PIPE_DIMENSIONS.filter((pipeObject, index, array) => pipeObject.standard === pipeStandard && pipeObject.nominalDiameter === pipeNominalDiameter))[0];
    //console.log(pipeObjectWithUserSelectedStandardAndDiameter);
    let arrayOfPipeSchedulePairs = Object.entries(pipeObjectWithUserSelectedStandardAndDiameter.wallThickness);
    let sortedLowToHighArrayOfPipeSchedulePairs = arrayOfPipeSchedulePairs.sort(function(a, b){return a[1]-b[1]});;
    //console.log(arrayOfPipeSchedulePairs);
    let arrayOfPipeSchedulesWithUserSelectedStandardAndDiameter = sortedLowToHighArrayOfPipeSchedulePairs.map((arr) => {return arr[1] ? arr[0] : ""}); // if no thickness specified for schedule, return return empty string which will be removed later on
    //console.log(arrayOfPipeSchedulesWithUserSelectedStandardAndDiameter);
    return arrayOfPipeSchedulesWithUserSelectedStandardAndDiameter;
};

